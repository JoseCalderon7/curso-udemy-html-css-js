//* CICLO WHILE ~ más común.
let contador = 0

while (contador < 3) {
    console.log("Número del contador: " + contador);
    contador++
}

//* CICLO DO-WHILE ~ menos recurrente.
do {
    console.log("Número del contador: " + contador);
    contador++
} while (contador < 6);


//* CICLO FOR 
for(let contador2 = 0; contador2 < 3; contador2++ ){
    console.log("Hola");
}

